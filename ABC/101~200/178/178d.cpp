/* nCr mod */
const int MAX = 510000;
long long fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}



int main(){
    long long S;
    cin >> S;

    COMinit();

    long long ans = 0;
    if(S>=3)ans += 1;

    for(long long i = 2; i*3<=S; i++){
        long long ball = S- i*3;
        long long box = i-1;

        ans += COM(ball+box,ball) %MOD;
    }
    cout << ans%MOD << endl;
}




